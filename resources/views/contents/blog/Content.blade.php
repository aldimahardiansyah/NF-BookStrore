                        <article>
                            <p style="text-align: justify;">
                                Liputan6.com, Jakarta - Ingin memberikan warna lain dalam memajukan pendidikan di
                                Indonesia menjadi alasan kuat Penerbit Erlangga mau jadi sponsor resmi Pekan Olahraga
                                Nasional atau PON XX Papua 2021.

                                Tidak hanya mendukung kegiatan belajar di sekolah, perusahaan yang menerbitkan buku-buku
                                pendidikan juga mendukung prestasi di bidang olahraga. PON XX Papua berlangsung sejak 2
                                hingga 15 Oktober 2021.

                                "Ada banyak nilai positif dalam olahraga. Untuk itu, ke depannya kami berkomitmen akan
                                terus medukung masyarakat Indonesia, terutama generasi muda lewat olahraga," kata
                                Manajer Marketing Nasional Penerbit Erlangga, Irving William P W dikutip dari keterangan
                                resmi yang diterima Health Liputan6.com pada Rabu, 29 September 2021.

                                Melalui dukungan ini, lanjut Irving, Penerbit Erlangga dapat memotivasi generasi muda
                                untuk mencintai olahraga, menumbuhkan rasa nasionalisme, dan memberikan semangat bagi
                                para atlet yang akan bertanding, serta semua yang terlibat dalam penyelenggaraan PON XX
                                Papua.

                                Senada dengan Irving, dokter spesialis anak Rumah Sakit Pondok Indah yang juga salah
                                seorang penulis Penerbit Erlangga, dr Ellen Wijaya SpA mengatakan bahwa Pekan Olahraga
                                Nasional atau PON merupakan satu perhelatan yang layak didukung seluruh lapisan
                                masyarakat.

                                Apalagi jika menyaksikan pertandingan di PON XX Papua 2021 bersama anak. Secara tak
                                langsung, tontonan tersebut bisa jadi wadah untuk memerkenalkan olahraga-olahraga serta
                                manfaatnya pada buah hati tercinta.

                                “Pekan Olahraga Nasional merupakan momen tepat untuk memerkenalkan aneka jenis olahraga
                                kepada anak serta membagikan semangat kompetitif yang sportif. Ajak anak berolahraga
                                secara rutin sesuai dengan usia dan kegemarannya. Tubuh yang sehat dan daya juang yang
                                tinggi adalah kunci anak cerdas dan berprestasi. Pada akhirnya, generasi muda yang
                                cerdas adalah kunci pembangunan bangsa," kata Ellen.

                                Persiapan PON XX Papua
                                Lebih lanjut, keterlibatan Penerbit Erlangga dalam ajang PON Papua XX 2021 sudah
                                dilakukan sejak Agustus 2021, dengan menyiapkan backdrop, spanduk, umbul-umbul di
                                sekitar lingkungan kantor perwakilan Erlangga, dan di sekolah-sekolah di sepanjang jalan
                                menuju lokasi pertandingan di Jayapura.

                                “Kemudian, melalui program Earpod di kanal Youtube Erlangga Inspirasi, kami menayangkan
                                talkshow dengan narasumber atlet dan penyanyi yang terlibat di PON XX Papua,” katanya.

                                Menteri Pemuda dan Olahraga, Zainudin Amali, mengapresiasi dan berterima kasih kepada
                                perusahaan yang telah mendukung PON XX Papua meskipun dalam situasi pandemi COVID-19.

                                “Tentu kita tidak bisa berharap secara maksimal karena sponsor itu kan selalu berkaitan
                                dengan penonton yang banyak memang mereka untuk spending dari pembiayaannya tidak ada
                                masalah tetapi menurut saya dengan keadaan sekarang tidak sebanyak dalam suasana
                                normal,” kata Amali.

                                Sementara itu, Koordinator Revenue PON XX Papua 2021, Vera Parinussa, mengatakan,
                                pihaknya sangat mengapresiasi dukungan Penerbit Erlangga. Vera juga meminta doa kepada
                                semua masyarakat agar penyelenggaraan PON XX Papua yang sebentar lagi berlangsung dapat
                                berjalan dengan lancar.

                                “Seperti yang kita ketahui, di ajang ini banyak atlet-atlet nasional yang bisa mendunia.
                                Sejauh ini persiapan kami sudah maksimal dan beberapa pertandingan juga sudah digelar,”
                                ujar Vera.

